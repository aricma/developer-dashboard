def hash_string_value(value: str) -> str:
    return str(hashlib.md5(value.encode("utf-8")).hexdigest())


def read_yml_file_content(path: Union[Path, str]) -> dict:
    with open(path, "r") as reader:
        return load(reader.read(), Loader=Loader)
